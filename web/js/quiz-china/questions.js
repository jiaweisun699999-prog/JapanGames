function rotateCorrectOption(q, targetIdx) {
  const opts = [...q.options];
  const correct = opts[q.answer];
  const wrong = opts.filter((_, i) => i !== q.answer);
  const out = [];
  let wi = 0;
  for (let p = 0; p < 4; p++) {
    if (p === targetIdx) out[p] = correct;
    else out[p] = wrong[wi++];
  }
  return { ...q, options: out, answer: targetIdx };
}

