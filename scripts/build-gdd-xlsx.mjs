function sheetFromAoA(name, rows) {
  const ws = XLSX.utils.aoa_to_sheet(rows);
  const colCount = Math.max(...rows.map((r) => r.length), 1);
  ws["!cols"] = Array.from({ length: colCount }, () => ({ wch: 18 }));
  return { name, ws };
}

